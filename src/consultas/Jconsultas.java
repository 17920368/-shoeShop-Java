        ca.addElement("Cantidad");
        ca.addElement("Codigo");
        ca.addElement("Modelo");
        ca.addElement("Color");
        ca.addElement("Precio");
        ca.addElement("Total");
        DefaultTableModel tabla= new DefaultTableModel(ca,0);
        int filas = Jconsultas.tabla_ventas.getRowCount();
