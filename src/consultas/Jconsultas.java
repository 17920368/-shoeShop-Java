                datos[3] = Jconsultas.tabla_ventas.getValueAt(i, 3).toString();
                datos[4] = Jconsultas.tabla_ventas.getValueAt(i, 4).toString();
                datos[5] = Jconsultas.tabla_ventas.getValueAt(i, 5).toString();
                datos[6] = Jconsultas.tabla_ventas.getValueAt(i, 6).toString();
                datos[7] = Jconsultas.tabla_ventas.getValueAt(i, 7).toString();
